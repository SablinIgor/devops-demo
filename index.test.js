const sum = require('./indexnew');

describe('Тестирование функции sum', () => {
  
  test('должна возвращать 3 при сложении 1 и 2', () => {
    expect(sum(1, 2)).toBe(3);
  });

  test('должна корректно работать с отрицательными числами', () => {
    expect(sum(-5, 10)).toBe(5);
  });

  test('должна возвращать то же число при сложении с нулём', () => {
    expect(sum(7, 0)).toBe(7);
  });
});
